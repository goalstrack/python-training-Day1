a='this is python code code'

print(len(a))#prints length of string

print(min(a))#space

print(max(a))#y

print(a.count('is'))

print(a.count('code'))


print(a.capitalize()) #first letter is capital

print(a.lower())#all lower characters

print(a.upper())#all uppercase

print(a.title())#first character of each word in uppercase

print(a.swapcase())#opposite case